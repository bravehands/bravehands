            <p><strong><em>1.	MOTTO OF THE NATIONAL YOUTH SERVICE CORPS</em></strong>            </p>
            <ul>
              <li> The Motto of the National Youth Service Corps (hereinafter referred to  as the &ldquo;Service Corps&rdquo;) shall be &ldquo;Service and Humility </li>
            </ul>
            <p><em><strong>2. THE PLEDGE</strong></em><strong></strong></p>
            <p>Every  member of the Service Corps (hereinafter referred to as &ldquo;member&rdquo;) shall  subscribe to and sign a copy of the National pledge and NYSC pledge  before the Chief Judge during Swearing-in Ceremony at the orientation  camp.<br>
              <br>
              Any member who fails or refuses to subscribe to and sign the National  and the NYSC pledges shall be tried by the Camp Court and, if found  guilty, be liable to be decamped and sent out of the orientation camp.  The member shall only be allowed to come back for service to join the  next batch for the orientation exercise only if he is ready to  subscribe to sign the pledges.<br>
            </p>
            <p><strong><em>3. CODE OF CONDUCT</em></strong></p>
            <ul>
              <em><strong>I.	DURING THE PERIOD OF ORIENTATION</strong></em><br>
              Every member shall:
  <ol>
    <li> Attend regularly, and participate, fully in all official engagements on  the field, at lectures and all places of work.<br>
        <ul>
          <li> Any member who comes late to any official engagement on the field, at  lectures or places of work shall be tried by the Camp Court and, if  found guilty, be liable to be decamped and sent out of the camp<br>
        </li>
          <li> The member shall only be allowed to come back for service by joining  the next batch for the orientation exercise. <br><br>
          </li>
        </ul>
    </li>
    <li> Not leave orientation camp or absent himself from any official activity  without the written consent of the State Coordinator but when absent  from his duties on account of illness, ensure that such absences is  covered by an &ldquo;Excuse Duty Certificate&rdquo; issued by a medical doctor then  on duty at the camp<br>
        <ul>
          <li> Any member who leaves the camp  without the permission from the State Coordinator shall be tried by the  Camp Court and, if found guilty, be liable to be decamped and sent out  of camp.<br>
        </li>
          <li> The member shall only be allowed to come back for service by joining the next batch for the orientation exercise.<br><br>
          </li></ul></li>
    <li> Not gamble within the premises.<br>
      <ul>
        <li> Any member who gambles shall be reported to the police for prosecution.</li>
        </ul>
    </li>
    <br>
    <li> Not be in possession of fire arms or ammunition
      <ul><br>
        <li> Any member who flouts this rule shall be reported to the police for prosecution. </li>
      </ul>
    </li>
    <br>
    <li> Not smoke, chew anything whatsoever, make or answer calls, while on  parade and at any other time, not smoke in a prohibited area or throw  lighted cigarette butts or lighted matches about the premises  carelessly.
      <ul><br>
        <li> Any member who flouts this rule  shall be tried by the Camp Court and, if found guilty, be liable to  extra drill for thirty (30) minutes.</li>
      </ul>
    </li>
    <br>
    <li> Not get drunk<br>
        <ul>
          <li> Any member found to be drunk shall be tried by the Camp Court and, if  found guilty, be liable to extra drill and/or confinement to the camp  rehabilitation room for thirty (30) minutes. </li>
      </ul>
    </li>
    <br>
    <li> Not receive visitors in the camp except on Saturdays, Sundays and public Holidays.<br>
        <ul>
          <li> Any member who flouts this rule shall be tried by the camp court and,  if found guilty, be liable to extra drill and/or confinement to the  camp rehabilitation room for thirty (30) minutes. </li>
      </ul>
    </li>
    <br>
    <li> Wear the various uniforms provided for the activities<br>
        <ul>
          <li> Any member who refuses, fails or neglects to comply with this rule  shall be tried by the Camp Court and, if found guilty, be liable to be  decamped and sent out of the orientation camp </li>
      </ul>
    </li>
    <br>
    <li> Not keep animal pets in camp<br>
        <ul>
          <li> Any member who brings animal pets to camp shall not be registered and if already registered, shall be decamped.</li>
      </ul>
    </li>
    <br>
    <li> Not take part in mob action<br>
        <ul>
          <li> Any member who takes part in riot or mob action shall be reported to the police for prosecution. </li>
      </ul>
    </li>
    <br>
    <li> Not steal.<br>
        <ul>
          <li> Any member who steals shall be reported to the police for prosecution </li>
      </ul>
    </li>
    <br>
    <li> Not take part in or organize night parties in camp without the written permission of the State Coordinator.<br>
        <ul>
          <li> Any member who fails, refuses or neglects to comply with this rule  shall be tried by the Camp Court and, if found guilty, be liable to  extra drill and/or confinement to the camp rehabilitation room for  thirty (30) minutes. </li>
      </ul>
    </li>
    <br>
    <li> Not be insubordinate or rude to camp officials<br>
        <ul>
          <li> Any member who is rude to camp officials shall be tried by the Camp  Court and, if found guilty, be liable to extension of service for (30)  days on half pay </li>
      </ul>
    </li>
    <br>
    <li> Not be in possession of Hard drugs like cocaine, Indian hemp, heroine etc.<br>
        <ul>
          <li> Any member who is in possession of Hard drugs shall be reported to the police for prosecution. </li>
      </ul>
    </li>
    <br>
    <li> Not bring vehicles or motorcycles into the orientation camp.<br>
        <ul>
          <li> Any member who brings vehicles or motorcycles to camp shall not be  registered, and if already registered, shall be decamped<br>
        </li>
          <li> ii)	Such member shall only be allowed to return to service to join the next batch in the orientation camp</li>
      </ul>
    </li>
    <br>
    <li> Not bring forged documents for registration<br>
        <ul>
          <li> Any member who brings forged document for registration shall be reported to the police for prosecution. </li>
      </ul>
    </li>
    <br>
    <li> Any member who brings forged document for registration shall be reported to the police for prosecution.<br>
        <ul>
          <li> Any member who organizes or engages in any form of secret cult activity  shall be immediately suspended and the case referred to police.<br>
        </li>
          <li> Such member shall be allowed to return to service only if he is  exonerated by the police investigation report. </li>
      </ul>
    </li>
    <br>
    <li> Not address the press on any policy issue without the prior written consent of the State Coordinator.<br>
        <ul>
          <li> Any member who addresses the press on any policy issue without the  written consent of the State Coordinator shall be tried by the Camp  Court and, if found guilty, be liable to extension of service for  thirty (30) days on half pay. </li>
      </ul>
    </li>
  </ol>
  <br>
  <em><strong>II. GENERAL NOTES</strong></em>
  <ol>
    <li> Disciplinary Bodies –<br>
        <ul>
          <li> During Orientation Course, the adjudicating body shall be the Camp Court.<br>
        </li>
          <li> During Primary Assignment, the adjudicating body shall be the Corps Disciplinary Committee<br>
        </li>
          <li> Drills and confinement to camp rehabilitation room (serving as  penalties) should be administered during camp leisure.</li><br>
      </ul>
    </li>
  </ol>
  <strong><em>
    III. DURING THE PERIOD OF PRIMARY ASSIGNMENT</em></strong><br>
              <br>Every member shall:<br>
              <li> Not refuse posting to primary assignment
                <ul>
                  <br>
                  <li> Any member who reports late at duty station shall be tried by the Corps  Disciplinary Committee and if found guilty, be liable to extension of  service on half pay double the period he is late to station. Any member  who refuses to accept posting to primary assignment shall forfeit the  allowance for the number of months out of work and also serve an  extension double the number of months out of work with half pay </li>
                </ul>
              </li>
    <br>
    <li> Not be late to report at duty station<br>
        <ul>
          <li> Any member who reports late at duty station shall be tried by the Corps  Disciplinary Committee and if found guilty, be liable to extension of  service on half pay double the period he is late to station. Any member  who reports late at duty station shall be tried by the Corps  Disciplinary Committee and if found guilty, be liable to extension of  service on half pay double the period he is late to station. </li>
        <br>
      </ul>
    </li>
    
    <li> Not fail to report to duty punctually.<br>
        <ul>
          <li> Any member who reports late for duty shall be queried by his employer  and the report sent to the State Coordinator for disciplinary action</li>
      </ul>
    </li>
    <br>
    <li> Not induce rejection of posting<br>
        <ul>
          <li> Any member who induces the rejection of his posting shall be liable on  conviction of extension of service for thirty days (30) on half pay </li>
        <br>
      </ul>
    </li>
    
    <li> Not fail to carry out duties diligently<br>
        <ul>
          <li> Any member who fails to carry out his duties diligently shall be  queried by his employer and the report sent to the State Coordinator  for disciplinary action </li>
      </ul>
    </li>
    <br>
    <li> Leave his duty station or absent himself from any official activity  without the written consent of the State Coordinator.<br>
        <ul>
          <li> Any member who leaves his duty  station or absents himself from any official activity without the  permission of the State Coordinator shall be tried by the Corps  Disciplinary Committee and, if found guilty, be liable to extension of  service with half pay double the period of absence. </li>
      </ul>
    </li>
    <br>
    <li> Not travel outside the State without written permission of the State Coordinator<br>
        <ul>
          <li> Any member who travels outside the State without the written permission  of the State Coordinator shall be tried by the Corps Disciplinary  Committee and, if found guilty, be liable to forfeiture of allowance  for the number of days absent and extension of service with half pay  double the period of absence </li>
      </ul>
    </li>
    <br>
    <li> Not be rude to constituted authority<br>
        <ul>
          <li> Any member who is rude to constituted authority shall be tried by the  Corps Disciplinary Committee and, if found guilty, be liable to  extension of service for a period not less than thirty (30) days with  half pay </li>
      </ul>
    </li>
    <br>
    <li> Not engage in immoral practice especially with students
        <br>
        <ul>
          <li> Any member who engages in immoral practices shall be tried by the Corps  Disciplinary Committee and, if found guilty, be liable to extension of  service for thirty (30) days without pay </li>
      </ul>
    </li>
    <br>
    <li> Not disclose official information<br>
        <ul>
          <li> Any member who discloses official information shall be tried by the  Corps Disciplinary Committee and, if found guilty, be liable to  extension of service for a period not less than thirty (30) days  without pay. </li>
      </ul>
    </li>
    <br>
    <li> Not leak examination question
        <br>
        <ul>
          <li> Any member who leaks examination question shall be reported to police for prosecution </li>
      </ul>
    </li>
    <br>
    <li> Not engage in private practice<br>
        <ul>
          <li> Any member who engages in private practice shall be tried by the Corps  Disciplinary Committee and, if found guilty, be liable on conviction to  extension of service for a period not less than twenty one (21) days  without pay </li>
      </ul>
    </li>
    <br>
    <li> Not take legal action against a fellow Corps Member, official of the  scheme or employer without first informing the Director-General for an  amicable settlement of the matter and also complying with S.20 of the  NYSC Act.
      <ul>
        <br>
        <li> If any member refuses, fails or  neglects to comply with this rule, his Certificate of National Service  shall be withheld till the determination of the case in Court </li>
      </ul>
    </li>
    <br>
    <li> Not travel outside Nigeria without the written permission of the Director-General<br>
        <ul>
          <li> Any member who travels outside Nigeria without the written permission  of the Director-General shall be tried by the Corps Disciplinary  Committee and, if found guilty, be liable to extension of service  without pay double the period of absence </li>
      </ul>
    </li>
  <br>
  <em><strong>IV. DURING THE PERIOD OF SECONDARY ASSIGNMENT </strong></em><br><br>
              Every member shall:<br>
              <li> Not be absent from the place of secondary assignment
                <ul>
                  <br>
                  <li> Any member who absents himself from the place of secondary assignment  for two weeks is liable to forfeiture of the month&rsquo;s allowance </li>
                </ul>
              </li>
    <br>
    <li> Not be late to the place of secondary assignment more than thirty (30) minutes<br>
        <ul>
          <li> Any member who is late to the place of secondary assignment for more  than thirty (30) minutes is liable to stern warning in the first  instance and if he continues to be late for four weeks, shall forfeit  his allowance (pro-rata) for seven days. </li>
      </ul>
    </li>
    <br>
    <li> Not solicit for fund from individuals, state and local government,  companies and multinationals or use any part of his personal allowance  to execute a project without the consent of the State Coordinator.
      <ul>
        <br>
        <li> Any  member who solicits for fund from individuals, State and Local  Governments, companies and multinationals, to execute a project without  the consent of the State Coordinator, shall be tried by the Corps  Disciplinary Committee and, if found guilty, shall be liable to the  following penalties:<br><br>
          i.	Non recognition of the project for any award; and<br>
          ii.	Extension of service for twenty-one (21) days without pay.</li>
      </ul>
    </li>
  <br>
  <em><strong>V.	DURING THE PERIOD OF WINDING UP AND PASSING-OUT</strong></em><br>
              <br>Every member shall:
  <ol><br>
    <li> Not fail to comply with a directive to participate in any of the winding up and passing-out activities<br>
        <ul>
          <li> Any member who fails, refuses or neglects to comply with the rule shall  forfeit the twenty-one (21) days terminal leave<br>
          </li>
      </ul>
    </li>
  </ol>
            </ul>
            <p><strong>4. MISCELLANEOUS OFFENCES AND PENALTIES</strong></p>
            <ul>
              Every member shall:<br>
              <li> Not lose the NYSC identity card
                <ul>
                  <li> Any member who loses the NYSC identity card is liable to a fine of five  hundred naira (N500.00) which shall be receipted. </li>
                </ul>
              </li>
              <br>
              <li> Follow the right channel of communication.
                <ul><br>
                  <li> Any member who fails, neglects or refuses to follow the right channel  of communication shall be tried by the Corps Disciplinary Committee  and, if found guilty, shall be liable to the following penalties:<br>
                    i.	No action shall be taken on such communication; and<br>
                    ii.	Extension of service for twenty-one (21) days with half pay. </li>
                </ul>
              </li>
              <br>
              <li> Not be involved in forgery
                <ul>
                  <li> Any member who is involved in forgery shall be reported to the police for prosecution </li>
                </ul>
              </li>
              <br>
              <li> Not be involved in kidnapping
                <ul>
                  <li> Any member who is involved in kidnapping shall be reported to the police for prosecution </li>
                </ul>
              </li>
              <br>
              <li> Not form any organization or group without the written consent of the State Coordinator<br>
                <ul>
                  <li> Any member who forms any organization or group without the consent of  the State Coordinator shall be liable to the following penalties:<br>
                    i.	Proscription of the group; and <br>
                    ii.	Extension of service for a period not less than fourteen (14) days with half pay </li>
                </ul>
              </li>
              <br>
              <li> Not be involved in fraud<br>
                <ul>
                  <li> Any member who is involved in fraud shall be reported to the police for prosecution </li>
                </ul>
              </li>
              <br>
              <li> Not engage in fighting<br>
                <ul>
                  <li> Any member who engages in fighting shall be liable to the following penalties:<br>
                    i)	Extension of service for a period not less than twenty one (21) days without pay.<br>
                    ii)	If the fight results in injury, the culprit shall bear the cost of medical treatment. </li>
                </ul>
              </li>
              <br>
              <li> Not assault NYSC official or any officer in his Place of Primary Assignment<br>
                <ul>
                  <li> Any member who assaults NYSC official or any officer in his Place of  Primary Assignment shall be liable to extension of service for a period  not less than twenty one (21) days without pay. </li><br>
                </ul>
              </li>
              
              <li> Not take part in partisan politics.<br>
                <ul>
                  <li> Any member who takes part in partisan politics is liable to extension  of service for a period not less than three (3) months without pay. </li>
                </ul>
              </li>
              <br>
              <li> Not lose any item of kits<br>
                <ul>
                  <li> Any member who loses any item of kits is liable to pay the equivalent  of the current price of the kit item for replacement. Such payment  shall be receipted </li>
                </ul>
              </li>
              <br>
              <li> Not fail to produce identity card on demand by NYSC official<br>
                <ul>
                  <li> Any member who fails, refuses or neglects to produce his identity card  on demand by NYSC official is liable to extension of service for a  period not less than twenty one (21) days with half pay </li>
                </ul>
              </li>
            </ul>
            <br>
            <p> <strong>5.	MEDICAL PROVISION:</strong></p>
            <ul>
              Any member who takes ill while on duty shall avail himself of the  nearest Military or Government Medical Facilities and on no account  seek specialist treatment outside his station without:<br>
              (a)	Prior reference by the Government or Military Doctor.<br>
              (b)	The written permission of the State Coordinator
            </ul>
            <strong><br>
            </strong>
            <p><strong>6.	LEAVE OF ABSENCE</strong></p>
            <ul>
              <li> Except in the case of the 21-day vacation granted as a terminal benefit  on completion of the service, leave is a privilege granted at the  discretion of the Director-General in accordance with the leave scales  laid down by the National Directorate Headquarters from time to time. </li>
              <br>
              <li> Except in the case of vacation leave, total number of days that can be  granted leave, for whatever reason, shall not exceed 14 days in any  service year. </li>
              <br>
              <li> Leave of absence shall not be granted to enable a member to travel outside, except by the Director-General </li>
              <br>
              <li> Subject to the exigencies of the service, a member may be recalled from his leave at short notice. </li>
              <br>
              <li> Leave of absence shall begin on the day specified in the letter  approving such leave and the member is required to rejoin his post on  the day the leave expires </li>
              <br>
              <li> On rejoining the post from  leave, it shall be the duty of each member to acquaint himself with all  instructions issued during his absence. </li>
              <br>
              <li> Where a member on leave  requires medical attention, he shall report to a Military or Government  Hospital and shall be treated on production of his identity card. </li>
              <br>
              <li> Where, in the absence of the  facilities referred to in sub-paragraph (g) above, a member is  compelled to avail himself of the services of a private medical  practitioner, he shall be responsible, in the first instance, for the  fees payable and shall obtain a receipt thereof as well as a medical  report from a Military or Government Hospital. </li>
              <br>
              <li> If the circumstances are  recognized as requiring such service, the National Directorate  Headquarters may authorize a refund to such a member at such rates as  the Director-General considers fair and reasonable. </li>
              <br>
              <li> A member who has availed  himself of the facilities in sub-paragraph (h) and (i) above must  tender a medical report or discharge certificate on disposal of the  case as evidence to return to his post. </li>
              <br>
              <li> Any payment made to a chemist  for treatment rendered to a member while on leave may only be refunded  by the State Coordinator, where there is no Hospital in the area, at  such rates as the State Coordinator considers fair and reasonable </li>
              <br>
              <li> Where a member is admitted into Hospital during his leave, it shall be his duty to notify his State Coordinator </li>
            </ul>
            <p><strong>7.	MATERNITY LEAVE DURING THE SERVICE YEAR</strong></p>
            <ul>
              <li> A married or an unmarried pregnant member shall be entitled to twelve  weeks maternity leave. During this period of leave, she will be paid  full monthly allowance. </li><br>
              <li> She will not, however, be entitled to the annual leave of 21days (terminal leave). </li>
            </ul>
            <p><strong>8.	MARRIAGE DURING SERVICE YEAR</strong></p>
            <ul>
              <li> A member who desires to get married during the service year shall be  allowed to perform the marriage ceremony in any place of his/her  choice. </li><br>
              <li> Any member who wishes to apply for leave for the purpose of  sub-paragraph (a) above shall do so, not later than, four weeks before  the date of the proposed marriage <br>
            </li>
            </ul>
            <p> <strong>9.	ABSCONDMENT</strong></p>
            <ul>
              Any member who is absent for continuous period of 3 months within the  service year shall be considered to have absconded and shall be  declared a wanted person.<br>
            </ul>
            <p><strong>10.	IDENTITY CARDS</strong></p>
            <ul>
              Every member shall:<br><br>
              <li> During the service year carry on his person, his identity card wherever  he goes and shall on request present it to the appropriate authority. </li><br>
              <li> At the end thereof, surrender it to the State Coordinator. </li></ul>
            <p><strong>11.	CONFIDENTIAL REPORTS</strong></p>
            <ul>
              In order to provide a full record of work, conduct and capabilities of  each member, and to assist the Directorate Headquarters to give a  correct assessment of each member at the end of the service year, the  following reports shall be made for each corps member:<br><br>
              <li> A record of Orientation Assessment documented on NYSC Form 2(A); </li><br>
              <li> A quarterly record of Primary Assignment Assessment by the Corps Employer documented on NYSC Form 2(B); and </li><br>
              <li> A record of Secondary Assignment Assessment by the Community Development Inspector documented on NYSC Form 2(C).  (NYSC Forms 2(A), (B) and (C) are in Schedules 3,4 and 5 respectively of these Bye-Laws) </li><br>
              (c) Each confidential report submitted pursuant to sub-paragraph (a)  above shall be considered a privileged document and shall not be made  public but:<br><br>
  <li> The substance of any adverse comment on the work  or conduct of a member which is included in any such report on him  shall be conveyed to him in writing by the State Coordinator with the  object of encouraging him to overcome his shortcomings; and </li><br>
  <li> The fact that this action has been taken should be endorsed on the report itself.<br>
    When a member has any cause to make representation in respect of any  part of his report, he shall do so through his Employer to the State  Coordinator. </li>
            </ul>
            <p><strong>12. CHANNELS OF COMMUNICATION</strong></p>
            <ol>
              <li> All communications including those for relocation from a Corps member  shall always be endorsed by his Employer. </li><br>
              <li> A member shall seek redress for any grievance or relocation from:
                <ul><br>
                  <li> His Corps Employer or the State Coordinator in the first instance; but </li><br>
                  <li> Where he is not satisfied with the action taken by any person mentioned  in sub-paragraph (i) above, he may appeal to the Chairman of the State  Governing Board; and </li><br>
                  <li> As a last resort, he may appeal to the Director-General</li>
                </ul>
              </li>
            </ol>
            <p> <strong>13.	EXPLANATORY NOTES</strong></p>
            <ul>
              <li> Any member who infringes any of the forgoing provisions shall have the  benefit of appearing either before the camp court during the  orientation/passing-out periods or the State Disciplinary Committee at  any other period(s) during the service year. The Court/Disciplinary  committee shall be established in each camp or State Secretariat to  determine breaches and mete out appropriate penalties. </li><br>
              <li> Except where otherwise stated, extension of service is now double the  period of absence from duty, with half pay or without pay as the case  may be, for the period </li><br>
              <li> Corps members should not travel out of the State during school/college  and public holidays, without permission from the State Coordinator </li>
            </ul></span>
            <p> <strong>14.	CITATION</strong></p>
            <ul>
              These Bye-Laws may be cited as the National Youth Service Corps Bye-Laws.
            </ul>
