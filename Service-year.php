<span id="about-nysc1"><p><strong><em>PRIMARY ASSIGNMENT</em></strong></p>
<div></div>
At the end of the orientation, Corps members are posted to their place  of primary assignment. While the NYSC Management takes into  consideration the areas of specialization of Corps members carrying out  the posting exercise, emphasis is placed in rural posting in the areas  of Agriculture, Health, Education and Infrastructure. <br>
<br>
It is expected that  Corps members should accept their posting and be agents of change  contributing towards the development of their host communities.
<div></div>
<p><strong><em>COMMUNITY DEVELOPMENT SERVICES</em></strong></p>
<div></div>
The Community Development Service (CDS) is aimed at harnessing the  skills, creativity and innovativeness of Corps members. Corps members  are expected to identified the needs of their host communities and  mobilise members of their host communities to embark on the projects. <br>
<br>
Through this programme,  many Corps members were able to construct bridges, health care centres,  classroom blocks, market stalls, culverts etc. they had also carried  out projects like HIV sensitization, adult literacy campaign,  extra-mural classes for students, road safety campaign etc. Corps  members participate actively in the Millennium Development Goals (MDG)  through the War Against Poverty (WAP) programme of the scheme.
<div></div>
<p><strong><em>WINDING –UP/PASSING-OUT</em></strong></p>
<div></div>
At the end of the service year (usually at the 12th month) corps  members are gathered in their respective zones for assessment of the  service year and de briefing. It is the period when Corps members  present their final clearance letters from their employers and submit  same to their Zonal/Local Government Inspectors. <br>
<br>
Parade rehearsals are  conducted in readiness for the passing-out ceremony which is usually  presided over by the Executive Governor of the State. During the  ceremony, those with outstanding performance during the service year  are given State Honours Award. For a Corps member to merit an award,  he/she must be outstanding in the orientation course, primary  assignment, community development service and winding –up exercise.</span>
