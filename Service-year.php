          <td width="75%"><strong><h3>NYSC SERVICE YEAR - AN OVERVIEW</h3></strong>          
            <p>The National Youth Service Corps Year comprises of four (4) main  segments in which every Corps Members must satisfactorily participate  before he/she is qualified to be issued a certificate of National  Service. The service year therefore comprises of: </p>
            <ul>
              <li> Orientation Courses<br/>
                <br/>
              </li>
              <li> Primary Assignment<br/>
                <br/>
              </li>
              <li> Community Development Service <br/>
                <br/>
              </li>
              <li> Winding – Up/Passing –out <br/>
                <br/>
              </li>
            </ul>
            <span id="about-nysc1"><p><em><strong>ORIENTATION COURSE</strong></em></p>
            <div></div>
A National Youth Service Corps year starts with a 3 weeks orientation  course and it is compulsory for all Nigeria graduates mobilised for  national service. The course lasts for three (3) weeks and is designed  to achieve the following objectives:</span>
<ul>
  <li> To give Corps Members a better understanding of the objectives of the  NYSC Scheme and enable them internalise its ideals <br/>
    <br/>
  </li>
  <li> To acquaint members with their environment in their political,  cultural, social and economic setting <br/>
    <br/>
  </li>
  <li> To prepare Corps members for their particular roles in the Scheme. <br/>
    <br/>
  </li>
  <li> To equip them with practical social and leadership skills that will  enable them meet the challenges of the service year ahead. <br/>
    <br/>
  </li>
  <li> To inculcate in the Corps members the spirit of national consciousness  as a basic ingredient in nation building. <br/>
    <br/>
  </li>
  <li> To instil discipline in the youths. <br/>
    <br/>
  </li>
  <li> To give Corps members adequate physical and mental training.<br/>
    <br/>
  </li>
  <li> To imbibe them with the Spirit of collective responsibility.<br/>
  </li>
</ul>
<span id="about-nysc1">To achieve the above objectives the orientation course opens with a  swearing –in/opening ceremony presided over by the executive Governor  of the State/FCT Minister. The oath of Allegiance and the National  Pledge are administered by the Chief Judge of the State/FCT.<br/>
<br/>
<div></div>
The orientation course content involves physical training, drills,  lectures on the people and tradition of the host State, professional  lectures for Corps health personnel&rsquo;s, lawyers, teachers and Skill  acquisition training e.t.c. Corps members participate in social  activities designed to create opportunities for them to interact.<br/>
<br/>
<div></div>
They participate in dance and drama competitions, Miss NYSC and Mr Macho.<br/>
<br/>
<div></div>
They also participate in inter platoon competitions on football,  volleyball, table tennis, cooking and sanitation.</span><br/>
<br/>
<div></div>
<p>Corps members are also trained on Man &lsquo;O&rsquo; War activities leading to  competitions on inter platoon drills and tug of war.</p>
