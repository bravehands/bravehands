          <td width="75%"><strong><h3>ABOUT THE NYSC SCHEME</h3></strong></p>
            <p>The NYSC scheme was created in a bid to reconstruct, reconcile and rebuild the country after the Nigerian Civil war. The unfortunate antecedents in our national history gave impetus to the establishment of the National Youth Service Corps by decree No.24 of 22nd May 1973 which stated that the NYSC is being established "with a view to the proper encouragement and development of common ties among the youths of Nigeria and the promotion of national unity".
            </p>
        <p>As a developing country. Nigeria is further plagued by the problems attendant upon a condition of under development, namely; poverty. mass illiteracy, acute shortage of high skilled manpower (coupled with most uneven distribution of the skilled people that are available), woefully inadequate socioeconomic infrastructural facilities, housing. Water and sewage facilities, road, healthcare services, and effective communication system. . Faced with these almost intractable problems, which were further compounded by the burden of reconstruction after the civil war, the government and people of Nigeria set for the country, fresh goals, and objectives aimed at establishing Nigeria as: </p>
        <p> 1. A united, strong and selfreliant nation<br/>
          <br/>
          2. a great and dynamic economy <br/>
  <br/>
          3. a land of bright and full opportunities for all citizens<br/>
  <br/>
          4. The government and people of Nigeria are not aware that sound and patriotic leadership is a precondition for the rapid social and economic development of the country. As a nation, Nigeria has been less fortunate in the kind of leadership that emerge to govern the affairs of the country in the period immediately after independence, a leadership whose achievements notwithstanding, was none the less ill-prepared. and generally not properly motivated to tackle the problems of socioeconomic under development, in the interest of the country as a whole. </p>
        <p>5. There is no gain saying the fact that the future of any country depends on the youths. The youths of Nigeria acknowledge this fact, and have consistently laid claim to the nation's leadership. <br/>
          <br/>
          6. While one may give credence to the saying that leaders are born, not made, one must also concede to the fact that leadership in a modem society requires a certain degree of preparation and orientation before the assumption of that role. <br/>
  <br/>
          7. The universities and other institutions of higher learning are normally expected to be training ground for future leaders, except that, as we are all aware, these institutions are first and foremost committed to the advancement of learning and knowledge, training of people for good citizenship. Little wonder that the products of these institutions have been accused of being too elitist in their outlook, of not identifying with the plight of common man, and of inability to appreciate predicament of the vast majority of our people who live in the rural areas. <br/>
  <br/>
          8. It was the need to look beyond the immediate present and to think of the future leadership of the country that necessitated the mobilisation of certain categories of our youths through the National Youth Service Corps Scheme. This was done with a view to giving them the proper guidance and orientation relevant to the needs of the country. The National Youth Setvice Corps Decree No. 24 which has now been repealed and replaced by Decree 51 of 16th June 1993, was then formally promulgated. <br/>
  <br/>
          9. The purpose of the scheme is primarily to inculcate in Nigerian Youths the spirit of selfless service to the community, and to emphasize the spirit of oneness and brotherhood of all Nigerians, irrespective of cultural or social background. The history of our country since independence has clearly indicated the need for unity amongst all our people, and demonstrated the fact that no cultural or geographical entity can exist in isolation. </p></td>
