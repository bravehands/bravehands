                <h2>NYSC FOUNDATION</h2></td>
              </tr>
          </table>
            <p><strong>The NYSC Foundation</strong> is essentially an expression of the resolve of a group of ex-corps memebers and public-spirited Nigerians to assist the Federal Government in sustaining the NYSC scheme and improving the quality and content of its impact on the lives of our youth. It is also to help our youth realize their potentials through the provision of employment and capacity building programme<br>
              <br>
              The foundation is an independent initiative and it is non-governmental,  officially registered under Part C of the companies and Allied Matters Acts, 1990. At present, the foundation has a Board of Trustees many up of men and women of integrity.<br>
  <br>
              The day-to-day operations of the foundation are carried out by the Excutive working committee headed by an Executive Secretary. The Executive Secretary is the head of the secretariat.<br>
  <br>
  <strong>AIMS AND OBJECTIVES OF THE FOUNDATION</strong><br>
  <br>
              Among the lofty aims and objectives of the foundation are:<br>
              (1) To foster healthy interaction amongst ex-corps members for national unity and integration.<br>
              (2) To harness the nations abundant human and material resources for the growth and development of the NYSC Scheme.<br>
              (3) To build a base of prospective financial support to assist ex-corps members in becoming self-employed.<br>
  <br>
  <strong>MEMBERSHIP AND ORGANIZATIONAL STRUCTURE</strong><br>
  <br>
              The Foundation has the following membership and organizational structure.<br>
  <em><strong><br>
    Ordinary Members:</strong></em> This category embraces all ex-corps members. They have no voting rights in matters relating to the Foundation.<br>
  <br>
  <em><strong>Associate Members:</strong></em> These are ex-corps members who have made a minimum financial contribution (5,000) five thousand naira only to the Foundation and are thus qualified to delibrate and act on issues affecting the Foundation.<br>
  <br>
  <strong><em>Corporate Members:</em></strong> This category consists of corporate organisations that have identified the objectives of the Foundation and have backed their support with contribution in cash or kind. They are qualified to attend the general meetings of the Foundation and they have voting rights. Corporate members will be encouraged to select one or more NYSC projects for direct funding and supervision. <br>
  <br>
  <em><strong>Honorary Members:</strong></em> This category consists of non-ex-corps members who identify with and support the scheme with financial and materials contributions.</p>
            <p><strong><em>Fellows of the Foundation</em>: </strong>Fellows are those Associate Members whose individual cumulative financial and material contributions over a period of time are of such a significant value to merit due recognition by the Foundation.<br>
              <br>
              <em><strong>Body of Patrons:</strong></em> The Foundation also has a body of patron made up of distinguished Nigerians and headed by the founding founder, General (Dr.) Yakubu Gowon. This Body of Patrons serves in an advisory capacity in the activities of the Foundation<br>
              <br>
              <em><strong>Grand Patrons</strong></em><strong>: </strong>His Excellency, the President and Commander -in-Chief of the Armed Force of the Federal Republic of Nigeria is the Grand Patron of the Foundation.<strong><br>
                <br>
                YOUTH EMPOWERMENT AND CAPACITY BUILDING PROGRAMMES</strong><br>
  <br>
              For some time now, the operation of the NYSC has becomes monotonous with emphasis only on mobilization, orientation, posting and passing out. In spite of all these, one fundamental problem which faces corps members is post service unemployment. One of the planned programme of the NYSC Foundation is to assist ex-corps members who are interested in self-employment with soft loans. This programme is adding more value to NYSC programme as well as reduces unemployment amongst youth. Like Alumni Associations of our higher institutions, the Foundation recruits members mainly from corps members passing out from servite (catching them young). Membership is voluntary and fee of Five hundred naira only is charged as membership fee.<br>
  <strong><br>
    ACCESSING LOAN FROM THE FOUNDATION</strong><br>
  <br>
              The first qualification an ex-corps member must possess before he/she could access loan facility from the Foundation is to be a member through subscription of a token fee of (#500).<br>
  <br>
              The second step is to prepare a feasibility report on a business plan he/she intends to establish and forward such, accompanied by an application to the Executive Secretary, NYSC Foundation, Abuja.<br>
  <br>
              It is noteworthy that the foundation gives a maximum credit limit of (#250,000) to a successful applicant with favourable repayment conditions in addition to being an interest-free facility.<br>
  <br>
          Since the commencement of its activities, a good number of ex-corps members have become self employed through the stress-free loan facility.</p>            <p>&nbsp;</p></td>
