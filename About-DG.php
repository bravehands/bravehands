          <td width="53%"><strong><h3><span class="innertitlebig">Profile Of Brig-Gen JB OLAWUMI FSS MSS CM psc (+) psc (joint) fdc (fellow Defence College)</span> </h3>
          </strong>
            <div class="sepdiv10"></div>
            <span id="about-nysc1">Brigadier-General Johnson Bamidele Olawumi hails from Iyin Ekiti, Ekiti State of Nigeria. </span></span> <span id="about-nysc1"><span class="innercontentsmall"> He attended the Army Children School, Mokola, Ibadan for his primary
education and thereafter proceeded to CAC Grammer School, Akure in Ondo
State for his secondary education.</span> <span class="innercontentsmall"> He enlisted into the Nigerian Army as a cadet of the 36 Regular Course
on 24 September 1984 and was commissioned into the Nigerian Army
Electrical and Mechanical Engineers' Corps on 23 September 1989. Since
commission, the senior officer has attended the following courses: </span></span><span class="innercontentsmall"></span>
<ul class="innerlist">
  <li> Regimental Intelligence and Security Officers' Course at The Nigerian
    Army Intelligence School, Apapa in 1990. </li><br>
  <li> Young Officers’ Course Infantry in Jaji, 1991. </li><br>
  <li> Anti Tank Platoon Commanders' Course in Jaji, 1991. </li><br>
  <li> EME Young Officers’ Course, NASEME Auchi in 1992. </li><br>
  <li> Junior Staff Course at Armed Forces Command and Staff College, Jaji in 1998. </li><br>
  <li> EME Company Commanders' Course, NASEME Auchi in the year 2000. </li><br>
  <li> Senior staff Course at Armed Forces Command and Staff College, Jaji
    from August 2002 to July 2003. </li><br>
  <li> Advanced Command and Staff Course at Shiverenham, United Kingdom from
    August 2005 to July 2006. </li><br>
  <li> National Defence College, Abuja from September 2010 to August 2011. </li><br>
</ul>
<span class="innercontentsmall"> His academics qualifications include: </span>
<ul class="innerlist">
  <li> B.Sc Mathemetics from NDA in 1988. </li><br>
  <li> B.Eng Mechanical Engineering from University of Ilorin in 1997. </li><br>
  <li> MA Defence Studies from the Kings College, University of London in 2006. </li><br>
</ul>
<span class="innercontentsmall"> The senior officer has served in various capacities across the country.
His postings and appoitments are as follows: </span>
<ul class="innerlist">
  <li> Scaling Officer, Technical Group, Yaba 1989-1990 </li><br>
  <li> Adjutant 22 Armoured Brigade Workshop, Ilorin 1990-1992 </li><br>
  <li> Production Officer, 4 Mechanical Brigade Workshop, Ilorin, 1997-1998. </li><br>
  <li> production officer, 1 Base Workshop, Yaba 1998-1999. </li><br>
  <li> Military Assistant to Commandant NASEME, 1999-2000. </li><br>
  <li> GSO 1 Trg/Ops, HQ NASEME, Lagos 2000-2002. </li><br>
  <li> Production Officer, 920 Div Workshop, Ibadan 2003-2004. </li><br>
  <li> Commanding Officer, Armed Forces Electrical and Mechanical Engineers Workshop, Abuja 2004. </li><br>
  <li> Directing staff, AFCSC, Jaji, 2004-2007 </li><br>
  <li> Commanding Officer, 950 Div Workshop, Enugu, 2007-2008. </li><br>
  <li> Deputy Director, Department of Logistics, Army headquarters Department
    of Policy and Plans, 2011-2012. </li><br>
  <li> Principal Staff Officer to the Chief of Army Staff 2013. </li>
</ul>
<span class="innercontentsmall"> Brigadier General Olawumi has enjoyed promotions as at when due since
his commision. He was promoted: </span>
<ul class="innerlist">
  <li> Lieutenant effective 24 Sep, 1988. </li><br>
  <li> Captain 24 Sep, 1992. </li><br>
  <li> Major 24 Sep, 1997. </li><br>
  <li> Lieutenant Colonel 24 Sep, 2002. </li><br>
  <li> Colonel 24 Sep, 2007. </li><br>
  <li> Brigadier General 24 Sep, 2012. </li>
</ul>
<span id="about-nysc1">His decorations and awards are :FSS, MSS, psc(+), psc (joint) and fdc.
(fellow Defence College).  Brigadier General Olawumi is happily married with children. He is
widely travelled and some of the countries he has visited are: UK, USA,
Germany, China, Singapore, Brazil, France, Italy, Kenya, Sudan,
Ethiopia, Benin Republic and Botswana. His hobbies include reading and
watching football. He is a strong supporter of Rangers International
FC, Enugu.</span></td>
