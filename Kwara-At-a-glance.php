            <br>
            <br>
The topography of the State is mainly plain lands. The prominent land forms are Sobi hill in Ilorin and Owu falls in Owakajola in Ifelodun Local Government Area. The major river is known as River Osin. There are two climate season, dry and wet seasons, and the average temperature ranges between 30°C and 36°C. The Climatic and Vegetation patterns make the state well suited for cultivation of a wide variety of crops such as yam, cassava, sugarcane, beans, fruits, rice and vegetables.<br>
<br>
<em><strong>Population</strong></em><br>
<br>
The Population of Kwara State is made up of majorly Yoruba, Nupe, Fulani and Baruba tribes. The populationn figure is estimated at 1,548,412 (1.5 millions).<br>
<br>
<em><strong>Mineral Resources</strong></em><br>
<br>
The mineral resources in the state include granite, limestone, kaoline, felispar, clay and quartz.<br>
<br>
<em><strong>Religion</strong></em><br>
<br>
The people of Kwara State often practice, Islam, Christianity and Traditional worship of Gods.
            <p><em><strong>Tourism</strong></em><br><br>
              Kwara State possess several captivating historical and culture monuments. They include the Esie Stone Images at Esie, Irepodun Local Government Area. The ages of the stone, which were said to be discovered in the 17th Century have not been determined, but it is believed that some of them are as old as creations. Others include the fascinating Owu falls in Irepodun Local Government Area, the remains of Mungo park wrecked boat and his monument both Lebba, Okuta and Norin where the name Norin was derived. Others are, Oke Agbonna Hills, which gives a wonderful view of the land, the Labba Dam and the University of Ilorin zoo. <br>
              <strong><em><br>
              Culture and Festival</em></strong><br><br>
              Kwara State is well known for its unique cultural diversity and the culture of the state, is as diverse as the people themselves. There are different traditional festivals held all over the state through the year. This cultural festival are often religeons purposes, ritualistic purposes or social purposes. They include; The Awon festival - Moro Local Government Area, The Patigi Regatta - Patigi Local Area, The Moremi festival and Egungun festival - Irepodun Local Government Area and the Eku Apa festival e.t.c<br><br>
              <em><strong>Pottery</strong></em><br><br>
              The most renowned of all places where pottery is being practiced is the Dada Centre in Ilorin. The pots which are of various sizes and designs are also made in Igbaja, Patigi and Lafiagi.<br><br>
              <em><strong>Education</strong></em><br><br>
              Education in Kwara State is a joint venture between the government, various communities and religious organizations. To show that education receives priority attention in the state. The Ministry of Education has six Directorates which handle specialized areas of education. These include promotion of science, technology, guidiance and counselling, special education and curriculum development.<br>
              <strong><em><br>
              Health Services</em></strong><br><br>
              Apart from private health and medical establishmnets in the state, there is University of Ilorin Teaching Hospital, Ilorin. Also, Four specialized Hospitals, Sixteen Cottage Hospitals, Fourteen General Hospitals and many Local Government Health Institutions.<br><br>
              <em><strong>Industrialization</strong></em><strong></strong><br><br>
              The State Government is obviously committed to the rapid industrialization of the state. Industrial estate are being established with necessary infrastructural facilities such as water, electricity and roads. Existing industries include; Kwara Breweries, Ijagbo International Tobacco Company, Pharmatech Nig. Limited all in Ilorin. Paper manufacturing industry, Jebba, Okin Biscuits, Ijagbo and Kwara Converters Limited Erin-Ile among others.<em><strong><br>
              <br>
              Information<br><br>
              </strong></em>Information Dissemination on government policies, is carried out through the State Information Department. There is also the government-owned Kwara Printing and Publishing Corporation, publishers of the Herald and Sunday Herald. The Kwara State Television service and the Kwara State Broadcasting Corporation (Radio Kwara), both of which are very much involved in information dissemination through its programmes in English and major languages of the state. The Nigeria Televison Authority (NTA) also has a branch in Ilorin, the state capital.<br><br>
              <em><strong>Sports</strong></em><br><br>
            Sporting activities have taken deep roots in the state through the State Sport Council. The importance of Sports to the State led to the construction of a stadium complex which include a mini bowl, an indoor sports hall as well as an Olympic size swimming pool.</p></td>
