          <td width="75%" valign="top"><strong><h3>KWARA STATE - AT A GLANCE</h3></strong></p>
            <p>Kwara State popularly known as the gateway between Northern and Southern parts of Nigeria was created on the 27th of May, 1967, with eleven other States of the federation. At that time, it was called provinces. This name was later changed to Kwara the local name for River Niger.<br>
              <br>
The creation of more states in 1967, reulted in the excision of the three Igala Divisions, namely: Idah, Dekina and Ankpa from Kwara State and their merger with former Benue province to form Benue State.<br>
Another creation of States in August 1991 led to the excision of Kogi, Okehi, Okene, Yagba and Oyi Local Government Areas from Kwara State. They were merged with the three Igala Division earlier mentioned to form Kogi State while Borgu Local Government Area was merged with Niger State.<br>
<strong><em><br>
Local Government<br>
<br>
</em></strong>Kwara State is bounded in the North by Niger State, in the South by Osun State and Ekiti State, in the East by Kogi State and in the West by Oyo State. It has international boundary with Benin Republic along the North-Western part of the State in Baruten Local Government Area of the State.<br>
<br>
