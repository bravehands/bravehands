            <h3> NYSC VISION	STATEMENT </h3>
            <div></div>
            <p>To develop a sound and result oriented organization that is strongly  committed to its set objectives particularly those of national unity  and even development. <br/>
              <br/>
              An organization that is well motivated and  capable of bringing out the best qualities in our youths and imparting  in them the right attitude and values for nation-building. An  organization that serves as a catalyst to national development, and a  source of pride and fulfillment to its participating graduate youths </p>
            <div></div>
            <h3>NYSC MISSION STATEMENT</h3>
            <div></div>
To build a pragmatic organization that is committed to its set  objectives with the ultimate goal of producing future leadership with  positive national ethos-Leadership that is vibrant, proud and committed  to the unity and even development of the Nigeria State. <br/>
<br/>
To be at the fore front of National development efforts, as well as  serve as a profitable platform for imparting in our youths values of  nationalism, patriotism, loyalty and accountable leadership.<br/>
<br/>
To raise the moral tone of our youths by giving them opportunity to  learn about high ideals of national achievement, social and cultural  improvement.<br/>
<br/>
To develop in our youths attitude of mind acquired through shared  experience, and suitable training which would make them more amenable  to mobilization in the national interest. <br/>
<br/>
To ensure Nigerian youths acquire the spirit of self-reliance, a  reliable source of economic empowerment and effective participation in  nation building.<br/>
<br/>
To develop an organization that is alive to its responsibilities and  responsive to the needs of the country.
