         <span id="about-nysc1"> <td width="75%"><h3>OBJECTIVES OF THE NYSC SCHEME</h3>
            <p><br/>
              The objectives of the National Youth Service Corps Scheme are clearly  spelt out in Decree No.51 of 16th June 1993 as follows</p>
            <p>1. To inculcate discipline in Nigerian youths by instilling in them a  tradition of industry at work, and of patriotic and loyal service to  Nigeria in any situation they may find themselves. </p>
            <p>2. To raise the moral tone of the Nigerian youths by giving them the  opportunity to learn about higher ideals of national achievement,  social and cultural improvement </p>
            <p>3. To develop in the Nigerian youths the attitudes of mind, acquired  through shared experience and suitable training. which will make them  more amenable to mobilisation in the national interest </p>
            <p>4. To enable Nigerian youths acquire the spirit of self reliance by  encouraging them to develop skills for self employment </p>
            <p>5. To contribute to the accelerated growth of the national economy </p>
            <p>6. To develop common ties among the Nigerian youths and promote national unity and integration </p>
            <p>7.  To remove prejudices, eliminate ignorance and confirm at first hand the  many similarities among Nigerians of all ethnic groups </p>
            <p>8. To develop a sense of corporate existence and common destiny of the people of Nigeria. </p>
            <p>9. The equitable distribution of members of the service corps and the  effective utilisation of their skills in area of national needs </p>
            <p>10. That as far as possible, youths are assigned to jobs in States other  than their States of origin </p>
            <p>11. That such group of youths assigned to work together is as  representative of Nigeria as far as possible </p>
            <p>12.  That the Nigerian youths are exposed to the modes of living of the  people in different parts of Nigeria </p>
            <p>13.  That the Nigerian youths are encouraged to eschew religious intolerance  by accommodating religious differences </p>
            <p>14. That members of the service corps are encouraged to seek at the end of  their one year national service, career employment all over Nigeria,  thus promoting the free movement of labour </p>
          <p>15. That employers are induced partly through their experience with members  of the service corps to employ more readily and on a permanent basis,  qualified Nigerians, irrespective of their States of origin </p></td></span>
