          <td width="75%" valign="top"><h3><strong>SKILLS ACQUSITION AND ENTREPRENEURIAL DEVELOPMENT (SAED)</strong></h3>
            <strong></strong><br/>
            <em><strong>Functions of SAED include:</strong></em>
            <div></div>
            <ul>
              <li> Sensitize and mobilize 200, 000 young graduates for skill acquisition annually<br/>
                <br/>
              </li>
              <li> Facilitate the training and mentoring of 100, 000 young graduates in  skill acquisition and entrepreneurship development for self-reliance  annually <br/>
                <br/>
              </li>
              <li> Promote public-private partnership for entrepreneurship development and  self-reliance amongst Nigerian youths<br/>
                <br/>
              </li>
              <li> Promote documentation and sharing of best practices on youth empowerment in Nigeria<br/>
                <br/>
              </li>
              <li> Support evidence-driven advocacy efforts for favorable policies on  youth empowerment in Nigeria <br/>
                <br/>
              </li>
              <li> Sensitization and mobilization of corps members for enrollment into the  skill acquisition and entrepreneurship development program<br/>
                <br/>
              </li>
              <li> Development of a standard curriculum for the in-camp skill acqusition exercise<br/>
                <br/>
              </li>
              <li> Identification of organizations at state level to provide training and  mentoring in specific skills sets<br/>
                <br/>
              </li>
              <li> Attachment of the corps members to the various organizations for skill  acquisition and entrepreneurship development<br/>
                <br/>
              </li>
              <li> On-going monitoring and supervision of trainee corps members <br/>
                <br/>
              </li>
              <li> Facilitate access to available funding opportunities<br/>
                <br/>
              </li>
            </ul>
