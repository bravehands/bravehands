        <span id="about-nysc1">  <td width="75%"><strong>
          <h3>NYSC DECREE</h3>
          </strong>
           <span id="about-nysc1"><strong><em>SECTION 1</em></strong></span>
           <span id="about-nysc1"> <ul>
              <li> There is hereby established a scheme to be known as the National Youth  Service corps (in this Decree referred to as "the Service Corps").<br/>
                <br/>
              </li>
              <li> The service corps shall with a view to:<br/>
                <br/>
                <ul>
                  <li> The proper encouragement and development of common ties among the  youths of Nigeria <br/>
                    <br/>
                  </li>
                  <li> the promotion of national unity <br/>
                    <br/>
                  </li>
                  <li> development of the youths of Nigeria and Nigeria into a great and  dynamic economy, be charged with the functions as pertain to the  objectives of the service corps set out in subsection (3) of this  section.<br/>
                    <br/>
                  </li>
                </ul>
              </li>
              <li> The objectives of the service corps shall be:
                <ul><br>
                  <li> to inculcate discipline in Nigerian youths by instilling in them a  tradition of industry at work, and of patriotic and loyal service to  Nigeria in any situation they may find themselves <br/>
                    <br/>
                  </li>
                  <li> to raise the moral tone of the Niqerian youths by giving them the  opportunity to learn about higher ideals of national achievements,  social and cultural improvement <br/>
                    <br/>
                  </li>
                  <li> to develop in the Nigerian youths the attitudes of mind, acquired  through shared experience and suitable training, which will make them  more amenable to mobilization in the national interest <br/>
                    <br/>
                  </li>
                  <li> to enable Nigerian youths acquire the spirit of self reliance by  encouraging them to develop skills for self employment <br/>
                    <br/>
                  </li>
                  <li> to contribute to the accelerated growth of the national economy<br/>
                    <br/>
                  </li>
                  <li> to develop common ties among the Nigerian youths and promote national  unity and integration<br/>
                    <br/>
                  </li>
                  <li> to remove prejudices, eliminate ignorance-and confirm at first hand the  many similarities among Nigerians of all ethnic groups <br/>
                    <br/>
                  </li>
                  <li> to develop a sense of corporate existence and common destiny of the  people of Nigeria<br/>
                    <br/>
                  </li>
                </ul>
              </li>
              <li> In order to achieve the objectives in subsection of this section, the  service corps shall ensure<br/>
                <br/>
                <ul>
                  <li> the equitable distribution of members of the service corps and the  effective utilization of their skills in areas of national needs <br/>
                    <br/>
                  </li>
                  <li> that as far as possible, youths are assigned to jobs in States other  than their States of origin<br/>
                    <br/>
                  </li>
                  <li> that such group of youths assigned to work together is as  representative of Nigerian as far as possible<br/>
                    <br/>
                  </li>
                  <li> that the Nigerian youths are exposed to the modes of living of the  people in different parts of Nigeria<br/>
                    <br/>
                  </li>
                  <li> that the Nigerian youths are encouraged to eschew religious intolerance  by accommodating religious differences<br/>
                    <br/>
                  </li>
                  <li> that members of the service corps are encouraged to seek at the end of  their one year national service, career employment all over Nigeria,  thus promoting the free movement of labour <br/>
                    <br/>
                  </li>
                  <li> that employers are induced partly through their experience with members  of the service corps to employ more readily and on a permanent basis,  qualified Nigerians, irrespective of their States of origin<br/>
                    <br/>
                  </li>
                </ul>
              </li>
            </ul>
            <em><strong>SECTION 2</strong></em>
            <ul>
              <li> Subject to the provisions of this Decree, every Nigerian shall<br/>
                <br/>
                <ul>
                  <li> if, at the end of the academic year 1972-73 or, as the case may be, at  the end of any subsequent academic year, he shall have graduated at any  university in Nigeria<br/>
                    <br/>
                  </li>
                  <li> if, at the end of the academic year 19'74-75 or, as the case may be, at  the end of any subsequent year, he shall have graduated at any  university outside Nigeria <br/>
                    <br/>
                  </li>
                  <li> if, at the end of the academic year 1975-76 or, as the case may be, at  the end of any subsequent year, he shall have obtained the Higher  National Diploma, or such other professiona1 qualification as may be  prescribed<br/>
                    <br/>
                  </li>
                  <li> if, at the end of the academic year 1975-76 or, as the case may be, at  the end of any subsequent academic year up to the end of the 1983-84  academic year, he shall have obtained the National Certificate of  Education, be under an obligation, unless exempted under $subsection  (2) of this section or section 17 of this Decree, to make himself  available for service for a continuous period of one year from the date  specified in the call-up instrument served upon him<br/>
                    <br/>
                  </li>
                </ul>
              </li>
              <li> Notwithstanding the provisions of subsection (1) of this section, with  effect from 1st August, 1985, a person shall not be called upon to  serve in the service corps if; at the date of his graduation or  obtaining his diploma or other professional qualification -<br/>
                <br/>
                <ul>
                  <li> he is over the age of thirty<br/>
                    <br/>
                  </li>
                  <li> he has served in the armed forces of the Federation or the Nigeria  Police Force for a period of more than nine months <br/>
                    <br/>
                  </li>
                  <li> he is member of staff of any of the following, that is<br/>
                    <br/>
                    <ul>
                      <li> the Nigerian Security Organisation<br/>
                        <br/>
                      </li>
                      <li> the State Security Service <br/>
                        <br/>
                      </li>
                      <li> the National Intelligence Agency<br/>
                        <br/>
                      </li>
                      <li> the Defence Intelligence Service <br/>
                        <br/>
                      </li>
                    </ul>
                  </li>
                  <li> he has been conferred with any National Honour. <br/>
                    <br/>
                  </li>
                </ul>
              </li>
              <li> A person liable to be called upon to serve in the service corps shall  serve for a continuous period of one year as from the date specified in  the call-up instrument served upon him or, as the case may be, if a  general or special notice to that effect is gi.ven in the prescribed  manner; and when so called upon, such person shall make himself  available for service and shall present himself at such place and time  and to such person or authority as may be specified in the instrument  or notice. <br/>
                <br/>
              </li>
              <li> The President, Commander-in-Chief of the Armed Forces may, by Order  published it, the Gazette, extend the application of the provisions of  subsection (1) of this section to other categories of Nigerians who  shall have obtained such diplomas, certificates or other qualifications  as may "be prescribed in the Order from universities, colleges and  other institutions of higher learning within or outside Nigeria, and  the Order may specify different dates for call-up in relation to  different categories of those affected or to be affected by the Order. </li>
            </ul>
            <strong><em><br>
SECTION 3</em></strong>
            <ul>
              <li> There shall be established, a national Directorate in the Presidency,  (in this Decree referred to as "the Directorate") which shall be the  governing body of the service corps. <br/>
                <br/>
              </li>
              <li> The Directorate shall have a Governing Board which shall comprise the  following ten members, to be appointed by the President,  Commander-in-Chief of the Armed Forces, that is -<br/>
                <br/>
                <ul>
                  <li> a Chairman<br/>
                    <br/>
                  </li>
                  <li> one representative of the committee of Vice-Chancellors<br/>
                    <br/>
                  </li>
                  <li> one representative of the committee of Rectors of Polytechnics<br/>
                    <br/>
                  </li>
                  <li> one representative of the Chief of Army Staff<br/>
                    <br/>
                  </li>
                  <li> one representative of the Inspector-General of Police<br/>
                    <br/>
                  </li>
                  <li> one representative of the Nigerian Employers Consultative Association<br/>
                    <br/>
                  </li>
                  <li> three other persons, one of whom shall be a woman<br/>
                    <br/>
                  </li>
                  <li> the Director-General <br/>
                    <br/>
                  </li>
                </ul>
              </li>
              <li> A member of the Directorate other than an ex-officio member shall hold  office for a term of three years from the date of his appointment and  shall be eligible for reappointment for one further term only. </li>
            </ul>
            <br>
            <em><strong>SECTION 4</strong></em>
            <ul>
              <li> The Directorate shall-<br/>
                <br/>
                <ul>
                  <li> draw up detailed programmes of training and schedules which shall be  designed for achieving the objects of the service corps <br/>
                    <br/>
                  </li>
                  <li> ensure that such programmes and schedules are strictly adhered to';   maintain regular contacts with all universities, polytechnics and colleges in Nigeria and abroad where applicable<br/>
                    <br/>
                  </li>
                  <li> assign members of the service corps to the programmed work in the  States as provided by the Directorate <br/>
                    <br/>
                  </li>
                  <li> co-ordinate the work of the State Governing Boards in furtherance of  the objects of this Decree<br/>
                    <br/>
                  </li>
                  <li> Assess and review, from time to time, the progress of the service corps<br/>
                    <br/>
                  </li>
                  <li> Make and submit all statutory and other reports as may be required of  it to the presidency<br/>
                    <br/>
                  </li>
                </ul>
              </li>
              <li> The Directorate may delegate any of its foregoing functions to a State  Governing Board, provided that such delegation shall not prevent the  Directorate from exercising the functions so delegated <br/>
                <br/>
              </li>
              <li> Service in the Directorate is hereby declared as approved service  within the meaning of the Pensions Act <br/>
                <br/>
              </li>
              <li> The Directorate shall have power to appoint, (including power to  appoint on promotion and transfer and of confirmation of appointments)  advance, terminate and discipline employees holding or acting in any  office in the Directorate </li>
            </ul>
            <br>
            <em><strong>SECTION 5</strong></em>
            <ul>
              <li> There shall be for the service corps, a Director - General who shall be  appointed by the President, Commander-in-Chief of the Armed Forces <br/>
                <br/>
              </li>
              <li> The Director-General shall be the Chief Executive and shall be charged  with general responsibility for matters affecting the day-to-day  running of the service corps<br/>
                <br/>
              </li>
              <li> The Director-General shall be assisted by Directors at the National  Directorate Headquarters and State Co-ordinators at the State  Headquarters<br/>
                <br/>
              </li>
            </ul>
            <br>
            <em><strong>SECTION 6</strong></em>
            <ul>
              <li> There is hereby established for each State, a National Youth Service  Corps Governing Board with its office in the Office of the State  Governor (in this Decree referred to as "the State Governing Board"). <br/>
                <br/>
              </li>
              <li> The State Governing Board shall be responsible for- <br/>
                <br/>
                <br>
                <ul>
                  <li> arranging and providing accommodation, boarding, transportation, and  providing other facilities which are essential for the welfare of  members of the service corps and for the success of the scheme in the  State <br/>
                    <br/>
                  </li>
                  <li> assembling reports on members of the service corps and transmitting  them to the Directorate<br/>
                    <br/>
                  </li>
                  <li> arranging the provision of facilities for orientation courses and  winding-:up exercises; <br/>
                    <br/>
                  </li>
                  <li> compiling information on such opportunities as there may be for  employment in the State <br/>
                    <br/>
                  </li>
                  <li> deploying members of the service corps within the State.<br/>
                    <br/>
                  </li>
                </ul>
              </li>
              <li> The State Governing Board may delegate any of its functions specified  in subsection (2) of this section to the National Youth Service Corps  Local Government Committee established under section 8 of this Decree,  provided that such delegation shall not prevent the State Governing  Board from exercising the functions so delegated. </li>
            </ul>
            <br>
            <em><strong>SECTION 7</strong></em>
            <ul>
              <li> The State Governing Board in each State shall consist of the following  members, that is- <br/>
                <br/>
                <br>
                <ul>
                  <li> a Chairman who shall be appointed by the State Governor <br/>
                    <br/>
                  </li>
                  <li> a member of the Armed Forces of the Federation<br/>
                    <br/>
                  </li>
                  <li> a member of the Nigeria Police Force <br/>
                    <br/>
                  </li>
                  <li> five other persons who shall be appointed from among persons concerned  with the following sectors of the Nigerian economy at least one of whom  shall represent- <br/>
                    <br/>
                    <br>
                    <ul>
                      <li> commerce and industry <br/>
                        <br/>
                      </li>
                      <li> agriculture and natural resources <br/>
                        <br/>
                      </li>
                      <li> local government<br/>
                        <br/>
                      </li>
                      <li> education <br/>
                        <br/>
                      </li>
                    </ul>
                  </li>
                  <li> the State Co-ordinator of the service corps <br/>
                    <br/>
                  </li>
                </ul>
              </li>
              <li> All members of the State Governing Board, with the exception of the  State co-ordinator shall be appointed by the State Governor.<br/>
                <br/>
              </li>
              <li> The Governor of a State and the Chairman of the Federal capital  Territory, Abuja shall be required to give to the State Governing Board  a minimum annual subvention of N500,000 to enable it cater adequately  for the welfare needs of corps members deployed to the State and such  minimum subvention shall be provided before they come.<br/>
              </li>
          </ul></td></span>
