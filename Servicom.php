            <p><strong>SERVICOM</strong> mean Service Compact with all Nigerians. It is a social contract between the Federel Government and people of this country. It is also about value re-orientation and attitudinal change that will ultimately bring about timely, effective and transparent service delivery.<br>
              <br>
              <strong>PRINCIPLES OF SERVICOM</strong><br>
SERVICOM is hinged on four main principles:<br>
(1) Affirmation of commitment to the service of Nigeria Nation<br>
(2) Conviction that Nigeria can only realize its full potential if citizens receive prompt and efficient service from the state<br>
(3) Consideration for the needs and rights of all Nigerians to enjoy social and economic advancement<br>
(4) Dedication to deliver qualityservices to which citizens are entitled to in timely, fair, honest, effective and transparent manner<br>
<br>
<strong>SERVICOM AS IT AFFECTS NATIONAL YOUTH SERVICE CORPS (NYSC)<br>
</strong>NYSC is a service oriented organization. The contribution of the scheme is felt through services corps members render to their host communities in particular and the nation in general. It is in this context that SERVICOM is of utmost relevance to the cause of National Youth Service Corps.<br>
<br>
<strong>ROLE OF CORPS MEMBERS</strong><br>
Having highlighted NYSC as a service organization, the onus therefore rest on corps members to ensure that they render fair, prompt, effective, honest and transparent services to their host communities. It is pertinent to remind corps members that the relevance of NYSC scheme can only be sustained if and only if corps members stay in their place of primary assignment and render services that meet the yearnings and aspirations of their host communities. Corps members should therefore ensure that they; <br>
(1) Obey NYSC rules and regulations at all times<br>
(2) Do not lobby for posting<br>
(3) Accept posting <br>
(4) Do not lure NYSC officials to illegalities<br>
(5) Participate in the dissemination of SERVICOM tenets and principles by joining SERVICOM vanguard club, and above all, be patriotic.<br>
<br>
<strong>GRIEVANCE / REDNESS MECHANISM</strong> <br>
The beauty of SERVICOM is that it has grievance redress mechanism in case you feel dissatisfied with the service rendered to you. You should also know that other people have rights to complain, if the services you rendered fall short of their expectations.</p>
            <p>You can lodge your compaint through suggestion / compliant boxes at the zonal offices and NYSC secretariat. You equally have unfettered access to the State Coordinator, NYSC SERVICOM Nodal officer and other key officers at NYSC secretatriat and zonal offices, if you have genuine complaints to lodge.<br><br>
              <strong>INDIVIDUAL OR TRADITIONAL CDS PROJECT</strong><br>
            This is a project conceived by a corp member to be executed to improve the economic and socio-cultural development of his/her community. The corps memeber identifies the project which must be relevant to his / her host community and then write a proposal to the State NYSC coordinator through his / her employer and Local Government Inspector for aprroval. The proposal must contain the following information:<br>
            - Project title<br>
            - Nature of the problem which your project intend to solve<br>
            - Location of the project<br>
            - Target / Beneficiaries of the project<br>
            - Estimated cost of the project<br>
            - Source of funding<br>
            - Duration of project<br><br>
            When the project is approved, corps member must always send progress report on the execution of the project with all pictorial evidence to support the claims. This must be done up to the completion of the project. A prject must be approved befor a corps member embarks on it. <br>
            <strong><br>
            COMMISSIONING OF THE PROJECT</strong><br>
            Corps member must liase with the State Coordinator through his / her Local Government Inspector to fix date, time and likely invited guest for the commissioning of the project. On no account should a corp member fix a date and write the State Coordinator or any other important personality for the commissioning of any project.<strong><br>
            <br>
            FUNDING OF CDS PROJECT</strong><br>
            It is not the responsibility of corps members or their parents to fund their CDS projects. All corps members who want to embark on any project should mobilize the host community, Local Government or Corporate Organisations within the community where the project is to be sited to fund the project.<br>
